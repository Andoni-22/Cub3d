#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	else
	{	
		del(lst->content);
		free(lst);
		lst = lst->next;
	}
}
